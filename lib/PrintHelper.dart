import 'package:flutter/cupertino.dart';

void printFullResponse(String content) {
  const int chunkSize = 800; // Safe chunk size
  debugPrint("=== START ===");

  for (int i = 0; i < content.length; i += chunkSize) {
    int end = (i + chunkSize < content.length) ? i + chunkSize : content.length;
    debugPrint(content.substring(i, end));
  }

  debugPrint("=== END ===");
}
